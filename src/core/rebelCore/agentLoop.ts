/**
 * Compact message history by truncating large tool_result outputs in older messages.
 * Keeps the last N turn pairs verbatim to preserve recent context.
 * Pure function — returns a new array, does not mutate input.
 */
export function compactMessageHistory(
  messages: ChatMessage[],
  preserveLastNTurns: number = PRESERVE_LAST_N_TURNS,
  forceCompactCurrentTurn = false,
): { compacted: ChatMessage[]; removedChars: number } {
  // Count turn pairs (assistant + user response). Walk backward to find the
  // boundary between "preserve" and "compact" regions.
  let turnPairCount = 0;
  let preserveFromIndex = 0; // default: preserve everything (idx >= 0 for all)
  for (let i = messages.length - 1; i >= 0; i--) {
    if (messages[i].role === 'assistant') {
      turnPairCount++;
      if (turnPairCount >= preserveLastNTurns) {
        preserveFromIndex = i;
        break;
      }
    }
  }

  let latestToolResultMessageIndex = -1;
  if (forceCompactCurrentTurn) {
    for (let i = messages.length - 1; i >= 0; i -= 1) {
      const message = messages[i];
      if (
        message.role === 'user'
        && Array.isArray(message.content)
        && message.content.some((block) => block.type === 'tool_result')
      ) {
        latestToolResultMessageIndex = i;
        break;
      }
    }
  }

  let removedTextChars = 0;
  let removedImageBlocks = 0;
  const compacted = messages.map((msg, idx) => {
    if (idx >= preserveFromIndex && idx !== latestToolResultMessageIndex) return msg;
    if (!Array.isArray(msg.content)) return msg;

    const hasLargeToolResult = msg.content.some(
      (block) => block.type === 'tool_result'
        && getCompactedToolResultContent((block as ToolResultBlock).content) !== null,
    );
    if (!hasLargeToolResult) return msg;

    const newContent = msg.content.map((block) => {
      if (block.type !== 'tool_result') return block;
      const tb = block as ToolResultBlock;
      const compactedToolResult = getCompactedToolResultContent(tb.content);
      if (!compactedToolResult) return block;
      removedTextChars += compactedToolResult.removedTextChars;
      removedImageBlocks += Math.max(
        countImageBlocks(tb.content) - countImageBlocks(compactedToolResult.content),
        0,
      );
      return {
        ...tb,
        content: compactedToolResult.content,
      };
    });
    return { ...msg, content: newContent };
  });

  const removedChars =
    removedTextChars + (removedImageBlocks * IMAGE_BLOCK_TOKEN_COST * APPROX_CHARS_PER_TOKEN);
  return { compacted, removedChars };
}

