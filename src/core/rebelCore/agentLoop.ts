function getRunawayPromptHardCap(contextWindow?: number): number {
  if (!contextWindow || contextWindow <= 0) return RUNAWAY_PROMPT_HARD_CAP_TOKENS;
  return Math.min(RUNAWAY_PROMPT_HARD_CAP_TOKENS, contextWindow * 2);
}

/**
 * Stage 1 — Deterministic universal output cap (the guarantee).
 *
 * Every freshly executed tool result flows through `executeToolUse` → this
 * transform before BOTH the persisted/UI event emit AND the model-facing
 * tool_result build. If the raw output exceeds `CONTENT_REF_THRESHOLD_BYTES`
 * (200 KiB), we replace it with a byte-bounded head preview plus a clear
 * truncation note. This is the class-eliminating safety net that guarantees no
 * tool_result exceeds the byte cap when sent to the model or persisted, so an
 * oversized output (e.g. raw image bytes decoded as UTF-8 by `Read`) can never
 * overflow context or re-enter `config.messages` on a later turn.
 *
 * Invariants:
 * - Unconditional and self-contained: works with NO workspace/cwd; NEVER falls
 *   back to emitting raw inline output. (File materialisation is Stage 3.)
 * - Byte-based: caps by `Buffer.byteLength(output, 'utf8')`, not char count.
 * - UTF-8-safe: the preview never splits a multi-byte character.
 * - Idempotent w.r.t. already-materialised output (`result.materialized ===
 *   true`, e.g. Bash's ~2 KB preview): such output is passed through untouched,
 *   never re-wrapped. We use the structured flag, not prose sniffing.
 * - Original size accounting (`outputChars`/originalBytes) is preserved by the
 *   caller; only the emitted/model-facing `output` string is shrunk.
 *
 * See docs/plans/260529_guard-large-tool-outputs/PLAN.md § Stage 1.
 */
// The truncation transform itself now lives in the shared, pure
// `@core/services/contentTruncation` module so Stage 1 (this funnel) and Stage 2
// (the provider translators) use ONE implementation. Re-exported here to keep
// the Stage 1 public surface (and its tests) stable.
export {
  UNIVERSAL_TOOL_OUTPUT_CAP_BYTES,
  boundToolOutputForSafety,
  type BoundedToolOutput,
};

/**
 * Screenshot variants of the vision-unsupported placeholder. Both branches end
 * with the shared `VISION_UNSUPPORTED_REPORT_INSTRUCTION` so the model reports
 * the limitation to its caller instead of silently working around it (the
 * historical failure mode: a non-vision model guesses the screenshot's
 * contents or quietly presses on without mentioning it couldn't look).
 */
const buildVisionUnsupportedPlaceholder = (index: number, screenshotPath?: string): string => {
  // "model", not "provider": gating is per-MODEL (supportsImageContent(model)).
  if (screenshotPath) {
    return `[Screenshot ${index + 1} at ${screenshotPath} - vision not supported by the current model; `
      + `use Read with the saved path. ${VISION_UNSUPPORTED_REPORT_INSTRUCTION}]`;
  }
  return `[Screenshot ${index + 1} captured - vision not supported by the current model; `
    + `no path was provided in tool output. ${VISION_UNSUPPORTED_REPORT_INSTRUCTION}]`;
};

export const buildModelFacingToolResultContent = (
  result: ToolExecutionResult,
  supportsImageContent: boolean,
  screenshotPath?: string,
): ToolResultBlock['content'] => {
  // Keep image/tool-result handling aligned with docs/project/UI_CHIEF_DESIGNER_VISUAL_VERIFICATION.md.
  const imageContent = result.imageContent ?? [];
  if (imageContent.length === 0) {
    return result.output;
  }

  if (supportsImageContent) {
    // Universal inline-image backstop (Stage 4 #2): ANY image block — from Read,
    // Screenshot, an MCP tool, or a fallback — is checked against the provider's
    // encoded-size / dimension limits here, mirroring the universal text cap. An
    // oversized block is reduced to a text placeholder so it can never reach a
    // provider and trigger a rejection, regardless of which tool produced it.
    return [
      { type: 'text', text: result.output },
      ...imageContent.map((image, index) => {
        const verdict = checkInlineImageWithinLimits(image.data, image.mimeType);
        if (!verdict.ok) {
          return {
            type: 'text' as const,
            text: buildOversizedImagePlaceholder(index, verdict.reason),
          };
        }
        return {
          type: 'image' as const,
          data: image.data,
          mimeType: image.mimeType,
        };
      }),
    ];
  }

  return [
    { type: 'text', text: result.output },
    ...imageContent.map((_, index) => ({
      type: 'text' as const,
      text: buildVisionUnsupportedPlaceholder(index, screenshotPath),
    })),
  ];
};

const getCompactedToolResultContent = (
  content: ToolResultBlock['content'],
): { content: ToolResultBlock['content']; removedTextChars: number } | null => {
  if (typeof content === 'string') {
    if (content.length <= TOOL_OUTPUT_TRUNCATION_THRESHOLD) return null;
    return {
      content: [{ type: 'text', text: COMPACTED_TEXT_PLACEHOLDER }],
      removedTextChars: content.length,
    };
  }

  if (!Array.isArray(content)) return null;

  const textBlocks: Array<Extract<ToolResultContentBlock, { type: 'text' }>> = [];
  let totalTextChars = 0;
  let imageBlockCount = 0;

  for (const block of content) {
    if (block.type === 'text') {
      textBlocks.push(block);
      totalTextChars += block.text.length;
    } else if (block.type === 'image') {
      imageBlockCount += 1;
    }
  }

  const shouldTruncateText = totalTextChars > TOOL_OUTPUT_TRUNCATION_THRESHOLD;
  const hasImages = imageBlockCount > 0;
  if (!shouldTruncateText && !hasImages) return null;

  return {
    content: [
      ...(shouldTruncateText ? [{ type: 'text' as const, text: COMPACTED_TEXT_PLACEHOLDER }] : textBlocks),
      ...Array.from({ length: imageBlockCount }, () => ({ type: 'text' as const, text: COMPACTED_IMAGE_PLACEHOLDER })),
    ],
    removedTextChars: shouldTruncateText ? totalTextChars : 0,
  };
};

const countImageBlocks = (content: ToolResultBlock['content']): number => {
  if (!Array.isArray(content)) return 0;

  return content.reduce((count, block) => count + (block.type === 'image' ? 1 : 0), 0);
};

