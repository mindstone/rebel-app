#!/usr/bin/env npx tsx
/**
 * IPC Bridge Generator
 *
 * Reads the IPC contract definitions and generates:
 * 1. src/preload/generated/ipcBridge.ts - Typed preload bridge (all domains)
 *
 * Usage:
 *   npx tsx scripts/generate-ipc-bridge.ts
 *   npm run generate:ipc
 */

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const GENERATED_DIR = path.join(ROOT, 'src/preload/generated');
const BRIDGE_FILE = path.join(GENERATED_DIR, 'ipcBridge.ts');

type DomainChannels = Record<string, ChannelDef>;

interface ChannelDef {
  type?: 'invoke' | 'sync';
  channel: string;
  description?: string;
  request: unknown;
  response: unknown;
}

/**
 * Load the TypeScript contract module via tsx
 */
export async function loadContract(): Promise<{ ipcContract: Record<string, DomainChannels> }> {
  const contractPath = path.join(ROOT, 'src/shared/ipc/contracts.ts');
  const module = await import(contractPath);
  return module as { ipcContract: Record<string, DomainChannels> };
}

/**
 * Generate TypeScript code for the preload bridge
 */
export function generateBridgeCode(domainEntries: [string, DomainChannels][]): string {
  const domainSections = domainEntries.map(([domain, channels]) => generateDomainSection(domain, channels));
  const legacyBlock = generateLegacyBlock();

  return `/**
 * AUTO-GENERATED FILE - DO NOT EDIT DIRECTLY
 *
 * Generated by: scripts/generate-ipc-bridge.ts
 * Source: src/shared/ipc/contracts.ts
 *
 * Regenerate with: npm run generate:ipc
 */

import { ipcRenderer } from 'electron';
import type { IpcRequestOf, IpcResponseOf } from '@shared/ipc/contracts';

${domainSections.map((section) => section.code).join('\n\n')}

// =============================================================================
// Legacy Compatibility Layer
// =============================================================================

/**
 * Legacy flat API surface for backward compatibility.
 * New code should use domain-specific APIs (workspaceApi, settingsApi, etc.).
 *
 * @deprecated Use domain-specific APIs instead
 */
export const legacyApiMethods = {
${legacyBlock}
};

export type LegacyApiMethods = typeof legacyApiMethods;
`;
}

function generateDomainSection(domain: string, channels: DomainChannels): { code: string } {
  const apiName = `${domain}Api`;
  const typeName = `${capitalize(domain)}Api`;
  const meta = DOMAIN_DESCRIPTIONS[domain] ?? defaultDomainMeta(domain);

  const methodStrings = Object.values(channels).map((def) => {
    const methodName = channelToMethodName(def.channel);
    const isSync = def.type === 'sync';

    if (isVoidSchema(def.request)) {
      if (isSync) {
        return `  ${methodName}: () =>\n    ipcRenderer.sendSync('${def.channel}') as IpcResponseOf<'${def.channel}'>,
`;
      }
      return `  ${methodName}: () =>\n    ipcRenderer.invoke('${def.channel}') as Promise<IpcResponseOf<'${def.channel}'>>,
`;
    }

    const optional = isOptionalSchema(def.request);
    const param = optional ? `request?: IpcRequestOf<'${def.channel}'>` : `request: IpcRequestOf<'${def.channel}'>`;

    if (isSync) {
      return `  ${methodName}: (${param}) =>\n    ipcRenderer.sendSync('${def.channel}', request) as IpcResponseOf<'${def.channel}'>,
`;
    }
    return `  ${methodName}: (${param}) =>\n    ipcRenderer.invoke('${def.channel}', request) as Promise<IpcResponseOf<'${def.channel}'>>,
`;
  });

  return {
    code: `// =============================================================================\n// ${meta.title}\n// =============================================================================\n\n/**\n * ${meta.description}\n * Handler: ${meta.handler}\n */\nexport const ${apiName} = {\n${methodStrings.join('\n')}\n};\n\nexport type ${typeName} = typeof ${apiName};`,
  };
}

function generateLegacyBlock(): string {
  return LEGACY_METHOD_MAPPINGS.map(({ comment, entries }) => {
    const lines = entries.map(({ legacyName, target }) => `  ${legacyName}: ${target},`);
    return comment ? `  // ${comment}\n${lines.join('\n')}` : lines.join('\n');
  }).join('\n\n');
}

function channelToMethodName(channel: string): string {
  const withoutPrefix = channel.replace(/^[^:]+:/, '');
  return withoutPrefix.replace(/-([a-z])/g, (_, letter) => letter.toUpperCase());
}

function isVoidSchema(schema: unknown): boolean {
  if (!schema || typeof schema !== 'object') return true;
  const def = (schema as { _def?: { typeName?: string } })._def;
  if (!def) return true;
  return def.typeName === 'ZodVoid' || def.typeName === 'ZodUndefined';
}

function isOptionalSchema(schema: unknown): boolean {
  if (!schema || typeof schema !== 'object') return false;
  const def = (schema as { _def?: { typeName?: string } })._def;
  if (!def) return false;
  return def.typeName === 'ZodOptional';
}

function capitalize(value: string): string {
  if (!value) return value;
  return value.charAt(0).toUpperCase() + value.slice(1);
}

function defaultDomainMeta(domain: string) {
  return {
    title: `${capitalize(domain)} API`,
    description: 'Domain API',
    handler: `src/main/ipc/${domain}Handlers.ts`,
  };
}

