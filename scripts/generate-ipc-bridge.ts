async function main() {
  console.log('🔧 IPC Bridge Generator');
  console.log('========================\n');

  console.log('📖 Loading IPC contract...');
  const contract = await loadContract();
  const domainEntries = Object.entries(contract.ipcContract);

  for (const [domain, channels] of domainEntries) {
    console.log(`   Found ${Object.keys(channels).length} ${domain} channels`);
  }
  const totalChannels = domainEntries.reduce((sum, [, channels]) => sum + Object.keys(channels).length, 0);
  console.log(`   Total: ${totalChannels} channels\n`);

  console.log('⚙️  Generating preload bridge...');
  const bridgeCode = generateBridgeCode(domainEntries);

  await fs.mkdir(GENERATED_DIR, { recursive: true });

  await fs.writeFile(BRIDGE_FILE, bridgeCode, 'utf8');
  console.log(`   ✅ Written: ${path.relative(ROOT, BRIDGE_FILE)}`);

  console.log('\n✨ Done!\n');
}

// Guard: only run main() when this script is executed directly, not when imported
// by other scripts (e.g. validate-ipc.ts imports generateBridgeCode).
const isDirectRun = process.argv[1] &&
  fileURLToPath(import.meta.url) === path.resolve(process.argv[1]);
if (isDirectRun) {
  main().catch((error) => {
    console.error('❌ Generator failed:', error);
    process.exit(1);
  });
}


