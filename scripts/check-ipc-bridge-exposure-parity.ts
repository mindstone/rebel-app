function resolvePaths(
  repoRoot: string,
  overrides?: Partial<IpcBridgeExposureParityPaths>,
): IpcBridgeExposureParityPaths {
  const merged: IpcBridgeExposureParityPaths = { ...DEFAULT_PATHS, ...(overrides ?? {}) };
  return {
    ipcBridgeFile: resolve(repoRoot, merged.ipcBridgeFile),
    preloadFile: resolve(repoRoot, merged.preloadFile),
    rendererEnvFile: resolve(repoRoot, merged.rendererEnvFile),
  };
}

function createSourceFile(filePath: string, source: string): ts.SourceFile {
  return ts.createSourceFile(filePath, source, ts.ScriptTarget.Latest, true, ts.ScriptKind.TS);
}

function hasExportModifier(node: ts.Node): boolean {
  return ts.canHaveModifiers(node)
    && (ts.getModifiers(node) ?? []).some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword);
}

function getPropertyNameKey(name: ts.PropertyName): string | null {
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNoSubstitutionTemplateLiteral(name)) {
    return name.text;
  }
  if (ts.isComputedPropertyName(name)) {
    const expression = name.expression;
    if (ts.isStringLiteral(expression) || ts.isNoSubstitutionTemplateLiteral(expression)) {
      return expression.text;
    }
  }
  return null;
}

function getLine(sourceFile: ts.SourceFile, node: ts.Node): number {
  return sourceFile.getLineAndCharacterOfPosition(node.getStart(sourceFile)).line + 1;
}

function collectIpcBridgeExports(params: {
  filePath: string;
  source: string;
}): Set<string> {
  const sourceFile = createSourceFile(params.filePath, params.source);
  const exports = new Set<string>();

  for (const statement of sourceFile.statements) {
    if (!ts.isVariableStatement(statement) || !hasExportModifier(statement)) continue;
    for (const declaration of statement.declarationList.declarations) {
      if (ts.isIdentifier(declaration.name)) {
        exports.add(declaration.name.text);
      }
    }
  }

  return exports;
}

function isContextBridgeExposeInMainWorldCall(node: ts.CallExpression): boolean {
  if (!ts.isPropertyAccessExpression(node.expression)) return false;
  if (node.expression.name.text !== 'exposeInMainWorld') return false;
  return ts.isIdentifier(node.expression.expression) && node.expression.expression.text === 'contextBridge';
}

function collectContextBridgeExposures(params: {
  filePath: string;
  source: string;
  repoRoot: string;
}): Exposure[] {
  const sourceFile = createSourceFile(params.filePath, params.source);
  const exposures: Exposure[] = [];

  const visit = (node: ts.Node): void => {
    if (ts.isCallExpression(node) && isContextBridgeExposeInMainWorldCall(node)) {
      const [nameArgument, valueArgument] = node.arguments;
      if (nameArgument && (ts.isStringLiteral(nameArgument) || ts.isNoSubstitutionTemplateLiteral(nameArgument))) {
        exposures.push({
          name: nameArgument.text,
          valueExpression: valueArgument ? valueArgument.getText(sourceFile) : '<missing>',
          file: relative(params.repoRoot, params.filePath),
          line: getLine(sourceFile, node),
        });
      }
    }
    ts.forEachChild(node, visit);
  };

  visit(sourceFile);
  return exposures.sort((a, b) => a.name.localeCompare(b.name) || a.line - b.line);
}

function collectWindowFields(params: {
  filePath: string;
  source: string;
  repoRoot: string;
}): WindowField[] {
  const sourceFile = createSourceFile(params.filePath, params.source);
  const fields: WindowField[] = [];

  const visit = (node: ts.Node): void => {
    if (ts.isInterfaceDeclaration(node) && node.name.text === 'Window') {
      for (const member of node.members) {
        if (!ts.isPropertySignature(member)) continue;
        const name = getPropertyNameKey(member.name);
        if (!name) continue;
        fields.push({
          name,
          typeText: member.type ? member.type.getText(sourceFile) : '<missing>',
          file: relative(params.repoRoot, params.filePath),
          line: getLine(sourceFile, member),
        });
      }
    }
    ts.forEachChild(node, visit);
  };

  visit(sourceFile);
  return fields.sort((a, b) => a.name.localeCompare(b.name) || a.line - b.line);
}

function makeNameMap<T extends { name: string }>(entries: readonly T[]): Map<string, T> {
  return new Map(entries.map((entry) => [entry.name, entry]));
}

function hasKnownDebtWindowTypeAllowlist(
  name: string,
  knownDebtByName: Map<string, KnownDebtAllowlistEntry>,
): boolean {
  return knownDebtByName.get(name)?.allowMissingWindowType === true;
}

function hasKnownDebtExposureAllowlist(
  name: string,
  knownDebtByName: Map<string, KnownDebtAllowlistEntry>,
): boolean {
  return knownDebtByName.get(name)?.allowMissingExposure === true;
}

export function findIpcBridgeExposureParityViolations(
  options: FindIpcBridgeExposureParityViolationsOptions = {},
): FindIpcBridgeExposureParityViolationsResult {
  const repoRoot = options.repoRoot ? resolve(options.repoRoot) : REPO_ROOT;
  const paths = resolvePaths(repoRoot, options.paths);
  const readFile = options.readFile ?? ((absolutePath: string) => readFileSync(absolutePath, 'utf8'));

  const legacyAliases = options.legacyAliases ?? LEGACY_ALIAS_EXCEPTIONS;
  const nonIpcBridgeExposures = options.nonIpcBridgeExposures ?? NON_IPC_BRIDGE_EXPOSURES;
  const connectorDomains = options.connectorDomains ?? RENDERER_FACING_CONNECTOR_DOMAINS;
  const knownDebtAllowlist = options.knownDebtAllowlist ?? KNOWN_DEBT_ALLOWLIST;
  const mainOnlyBridgeExports = options.mainOnlyBridgeExports ?? MAIN_ONLY_BRIDGE_EXPORTS;

  const bridgeExports = collectIpcBridgeExports({
    filePath: paths.ipcBridgeFile,
    source: readFile(paths.ipcBridgeFile),
  });
  const exposures = collectContextBridgeExposures({
    filePath: paths.preloadFile,
    source: readFile(paths.preloadFile),
    repoRoot,
  });
  const windowFields = collectWindowFields({
    filePath: paths.rendererEnvFile,
    source: readFile(paths.rendererEnvFile),
    repoRoot,
  });

  const exposuresByName = makeNameMap(exposures);
  const windowFieldsByName = makeNameMap(windowFields);
  const legacyAliasByName = makeNameMap(legacyAliases);
  const nonIpcBridgeExposureByName = makeNameMap(nonIpcBridgeExposures);
  const knownDebtByName = makeNameMap(knownDebtAllowlist);
  const connectorDomainByName = makeNameMap(connectorDomains);
  const mainOnlyBridgeExportByName = makeNameMap(mainOnlyBridgeExports);

  const exposedWithoutBridgeExport = exposures
    .filter((exposure) => !bridgeExports.has(exposure.name))
    .filter((exposure) => !legacyAliasByName.has(exposure.name))
    .filter((exposure) => !nonIpcBridgeExposureByName.has(exposure.name));

  const exposedWithoutWindowType = exposures
    .filter((exposure) => !windowFieldsByName.has(exposure.name))
    .filter((exposure) => legacyAliasByName.get(exposure.name)?.allowMissingWindowType !== true)
    .filter((exposure) => nonIpcBridgeExposureByName.get(exposure.name)?.allowMissingWindowType !== true)
    .filter((exposure) => !hasKnownDebtWindowTypeAllowlist(exposure.name, knownDebtByName))
    .sort((a, b) => a.name.localeCompare(b.name) || a.line - b.line);

  const legacyAliasTargetViolations: string[] = [];
  for (const alias of legacyAliases) {
    const exposure = exposuresByName.get(alias.name);
    if (!exposure) continue;

    if (exposure.valueExpression !== alias.target) {
      legacyAliasTargetViolations.push(
        `${alias.name} should expose ${alias.target}, but exposes ${exposure.valueExpression}`,
      );
    }
    if (!bridgeExports.has(alias.target)) {
      legacyAliasTargetViolations.push(
        `${alias.name} targets ${alias.target}, but ${alias.target} is not exported by src/preload/ipcBridge.ts`,
      );
    }
  }
  legacyAliasTargetViolations.sort();

  const connectorDomainValueBindingViolations = connectorDomains
    .map((domain) => {
      const exposure = exposuresByName.get(domain.name);
      if (!exposure || exposure.valueExpression === domain.name) return null;
      return `${domain.name} should expose ${domain.name}, but exposes ${exposure.valueExpression}`;
    })
    .filter((violation): violation is string => violation !== null)
    .sort();

  const connectorDomainsMissingExport = connectorDomains
    .filter((domain) => !bridgeExports.has(domain.name));

  const connectorDomainsMissingExposure = connectorDomains
    .filter((domain) => !exposuresByName.has(domain.name))
    .filter((domain) => !hasKnownDebtExposureAllowlist(domain.name, knownDebtByName));

  const connectorDomainsMissingWindowType = connectorDomains
    .filter((domain) => !windowFieldsByName.has(domain.name))
    .filter((domain) => !hasKnownDebtWindowTypeAllowlist(domain.name, knownDebtByName));

  const bridgeExportClassificationConflicts: string[] = [];
  const unclassifiedBridgeExports: string[] = [];
  for (const exportName of [...bridgeExports].filter((name) => name.endsWith('Api')).sort()) {
    const buckets = [
      connectorDomainByName.has(exportName) ? 'renderer-facing' : null,
      mainOnlyBridgeExportByName.has(exportName) ? 'main-only' : null,
      legacyAliasByName.has(exportName) ? 'legacy-alias' : null,
      knownDebtByName.has(exportName) ? 'known-debt' : null,
    ].filter((bucket): bucket is string => bucket !== null);

    if (buckets.length === 0) {
      unclassifiedBridgeExports.push(exportName);
    } else if (buckets.length > 1) {
      bridgeExportClassificationConflicts.push(`${exportName}: ${buckets.join(', ')}`);
    }
  }

  const mainOnlyBridgeExportsExposed = mainOnlyBridgeExports
    .filter((entry) => exposuresByName.has(entry.name))
    .map((entry) => `${entry.name}: ${entry.reason}`)
    .sort();

  const staleAllowlistEntries: StaleAllowlistEntry[] = [];
  for (const entry of knownDebtAllowlist) {
    if (entry.allowMissingExposure && exposuresByName.has(entry.name)) {
      staleAllowlistEntries.push({
        name: entry.name,
        allowlist: 'knownDebtMissingExposure',
        reason: entry.reason,
      });
    }
    if (entry.allowMissingWindowType && windowFieldsByName.has(entry.name)) {
      staleAllowlistEntries.push({
        name: entry.name,
        allowlist: 'knownDebtMissingWindowType',
        reason: entry.reason,
      });
    }
  }
  for (const alias of legacyAliases) {
    if (alias.allowMissingWindowType && windowFieldsByName.has(alias.name)) {
      staleAllowlistEntries.push({
        name: alias.name,
        allowlist: 'legacyAliasMissingWindowType',
        reason: alias.reason,
      });
    }
  }
  for (const exposureException of nonIpcBridgeExposures) {
    if (exposureException.allowMissingWindowType && windowFieldsByName.has(exposureException.name)) {
      staleAllowlistEntries.push({
        name: exposureException.name,
        allowlist: 'nonIpcBridgeMissingWindowType',
        reason: exposureException.reason,
      });
    }
  }
  staleAllowlistEntries.sort((a, b) => a.name.localeCompare(b.name) || a.allowlist.localeCompare(b.allowlist));

  const warnings: string[] = [];
  if (staleAllowlistEntries.length > 0) {
    warnings.push(
      `${staleAllowlistEntries.length} allowlist entry/entries are stale. Remove resolved debt from the allowlist.`,
    );
  }

  return {
    bridgeExports: [...bridgeExports].sort(),
    exposures,
    windowFields,
    exposedWithoutBridgeExport,
    exposedWithoutWindowType,
    legacyAliasTargetViolations,
    connectorDomainValueBindingViolations,
    connectorDomainsMissingExport,
    connectorDomainsMissingExposure,
    connectorDomainsMissingWindowType,
    bridgeExportClassificationConflicts,
    unclassifiedBridgeExports,
    mainOnlyBridgeExportsExposed,
    staleAllowlistEntries,
    warnings,
  };
}

function formatExposure(exposure: Exposure): string {
  return `${exposure.file}:${exposure.line} ${exposure.name} -> ${exposure.valueExpression}`;
}

export function main(): void {
  const result = findIpcBridgeExposureParityViolations();

  for (const warning of result.warnings) {
    process.stderr.write(`[ipc-bridge-exposure-parity] warning: ${warning}\n`);
  }

  if (result.staleAllowlistEntries.length > 0) {
    process.stderr.write(
      `[ipc-bridge-exposure-parity] warning: ${result.staleAllowlistEntries.length} stale allowlist entry/entries:\n`,
    );
    for (const entry of result.staleAllowlistEntries) {
      process.stderr.write(`  - ${entry.name} (in ${entry.allowlist} allowlist): ${entry.reason}\n`);
    }
  }

  const errors: string[] = [];

  if (result.exposedWithoutBridgeExport.length > 0) {
    errors.push(
      `${result.exposedWithoutBridgeExport.length} preload exposure(s) without an ipcBridge export or documented exception:`,
    );
    for (const exposure of result.exposedWithoutBridgeExport) {
      errors.push(`  - ${formatExposure(exposure)}`);
    }
  }

  if (result.exposedWithoutWindowType.length > 0) {
    errors.push(
      `${result.exposedWithoutWindowType.length} preload exposure(s) without a Window ambient type or documented exception:`,
    );
    for (const exposure of result.exposedWithoutWindowType) {
      errors.push(`  - ${formatExposure(exposure)}`);
    }
  }

  if (result.legacyAliasTargetViolations.length > 0) {
    errors.push(`${result.legacyAliasTargetViolations.length} legacy alias target violation(s):`);
    for (const violation of result.legacyAliasTargetViolations) {
      errors.push(`  - ${violation}`);
    }
  }

  if (result.connectorDomainValueBindingViolations.length > 0) {
    errors.push(
      `${result.connectorDomainValueBindingViolations.length} renderer-facing bridge exposure value-binding violation(s):`,
    );
    for (const violation of result.connectorDomainValueBindingViolations) {
      errors.push(`  - ${violation}`);
    }
  }

  if (result.connectorDomainsMissingExport.length > 0) {
    errors.push(
      `${result.connectorDomainsMissingExport.length} renderer-facing bridge domain(s) without ipcBridge export:`,
    );
    for (const domain of result.connectorDomainsMissingExport) {
      errors.push(`  - ${domain.name}: ${domain.reason}`);
    }
  }

  if (result.connectorDomainsMissingExposure.length > 0) {
    errors.push(
      `${result.connectorDomainsMissingExposure.length} renderer-facing bridge domain(s) without preload exposure:`,
    );
    for (const domain of result.connectorDomainsMissingExposure) {
      errors.push(`  - ${domain.name}: ${domain.reason}`);
    }
  }

  if (result.connectorDomainsMissingWindowType.length > 0) {
    errors.push(
      `${result.connectorDomainsMissingWindowType.length} renderer-facing bridge domain(s) without Window ambient type:`,
    );
    for (const domain of result.connectorDomainsMissingWindowType) {
      errors.push(`  - ${domain.name}: ${domain.reason}`);
    }
  }

  if (result.bridgeExportClassificationConflicts.length > 0) {
    errors.push(
      `${result.bridgeExportClassificationConflicts.length} ipcBridge *Api export classification conflict(s):`,
    );
    for (const conflict of result.bridgeExportClassificationConflicts) {
      errors.push(`  - ${conflict}`);
    }
  }

  if (result.unclassifiedBridgeExports.length > 0) {
    errors.push(
      `${result.unclassifiedBridgeExports.length} unclassified ipcBridge *Api export(s) - classify as renderer-facing, main-only, legacy alias, or known-debt:`,
    );
    for (const exportName of result.unclassifiedBridgeExports) {
      errors.push(`  - ${exportName}`);
    }
  }

  if (result.mainOnlyBridgeExportsExposed.length > 0) {
    errors.push(`${result.mainOnlyBridgeExportsExposed.length} main-only ipcBridge *Api export(s) exposed to renderer:`);
    for (const violation of result.mainOnlyBridgeExportsExposed) {
      errors.push(`  - ${violation}`);
    }
  }

  if (errors.length > 0) {
    process.stderr.write('IPC bridge exposure parity check failed.\n');
    for (const line of errors) {
      process.stderr.write(`${line}\n`);
    }
    process.exit(1);
  }

  process.stdout.write(
    `IPC bridge exposure parity check passed (${result.bridgeExports.length} ipcBridge export${result.bridgeExports.length === 1 ? '' : 's'}, ${result.exposures.length} preload exposure${result.exposures.length === 1 ? '' : 's'}, ${result.windowFields.length} Window field${result.windowFields.length === 1 ? '' : 's'}, ${RENDERER_FACING_CONNECTOR_DOMAINS.length} required renderer-facing bridge domain${RENDERER_FACING_CONNECTOR_DOMAINS.length === 1 ? '' : 's'} scanned).\n`,
  );
  process.exit(0);
}

