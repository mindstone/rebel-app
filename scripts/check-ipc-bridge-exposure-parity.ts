/**
 * CI validation: preload IPC bridge export -> exposure -> Window type parity.
 *
 * Static analysis only:
 * - Parses `src/preload/ipcBridge.ts` to discover generated domain API exports.
 * - Parses `src/preload/index.ts` to discover contextBridge.exposeInMainWorld()
 *   preload exposures.
 * - Parses `src/renderer/env.d.ts` to discover ambient Window fields.
 *
 * Reports (blocking errors):
 * - exposed preload surface without an ipcBridge export or documented exception
 * - exposed preload surface without a Window ambient field or documented exception
 * - curated renderer-facing connector domain without the full export/expose/type chain
 *
 * Known debt allowlists are intentionally narrow. Do not extend them without
 * resolving the product decision captured in
 * docs/plans/260607_oss-scrub-regression-class/PLAN.md Stage 3.
 */

