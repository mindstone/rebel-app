/**
 * Type-safe mapping from a connector name (the user-supplied argument to
 * `npm run mcp:release <name>`) to:
 *
 *  - the npm package name on the registry,
 *  - the catalog ID(s) that pin the version.
 *
 * One source of truth: extending support for a new connector requires
 * appending a typed entry here. The release script consumes this table at
 * Stages 0 and 6.
 *
 * Note (2026-05-26): the Microsoft 5 previously carried a
 * `microsoftPrimaryConst` field for atomic update of
 * `MICROSOFT_REBEL_OSS_DEFS.packageSpec` alongside the catalog. As of v2 of
 * the release plan, MICROSOFT_REBEL_OSS_DEFS no longer carries `packageSpec`
 * — the catalog is the sole source of truth for OSS pins, with structured
 * errors on miss. The atomic-update branch in Stage 6 was removed.
 *
 * See docs/plans/260525_oss_release_automation.md (v2) for context.
 */

export interface ConnectorReleaseMapping {
  /**
   * The connector name passed on the CLI: `npm run mcp:release <name>`.
   * Must match the directory name under `mcp-servers/connectors/<name>/`.
   */
  readonly name: string;

  /**
   * The full npm package name (without version) as published on the
   * registry, e.g. `@mindstone/mcp-server-slack`.
   */
  readonly npmPackage: string;

  /**
   * The catalog IDs (from resources/connector-catalog.json) whose
   * `mcpConfig.args[1]` should be updated when this connector is released.
   * Most connectors map 1:1, but some legacy entries map to multiple IDs.
   */
  readonly catalogIds: readonly string[];

  /**
   * If true, this connector has not yet been published to npm.
   * The release script's first-publish path skips the version-already-
   * published check and bootstraps the catalog entry against 0.0.1.
   */
  readonly firstPublish?: boolean;
}

