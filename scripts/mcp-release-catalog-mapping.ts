export type ConnectorReleaseName = keyof typeof CONNECTOR_RELEASE_MAPPINGS;

export function getMapping(name: string): ConnectorReleaseMapping {
  const mapping = (CONNECTOR_RELEASE_MAPPINGS as Record<string, ConnectorReleaseMapping>)[name];
  if (!mapping) {
    const available = Object.keys(CONNECTOR_RELEASE_MAPPINGS).sort().join(', ');
    throw new Error(
      `Unknown connector "${name}". Add a mapping to scripts/mcp-release-catalog-mapping.ts. ` +
        `Available connectors: ${available}.`,
    );
  }
  return mapping;
}

export function listConnectorNames(): ConnectorReleaseName[] {
  return Object.keys(CONNECTOR_RELEASE_MAPPINGS) as ConnectorReleaseName[];
}
